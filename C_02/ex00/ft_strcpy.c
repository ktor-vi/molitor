int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	len;

	len = ft_strlen(src);
	while (len >= 0)
	{
		dest[len] = src[len];
		len--;
	}
	return (dest);
}
