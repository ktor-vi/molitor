int	ft_is_prime(int nb)
{
	int	i;
	int	result;

	i = nb - 1;
	result = 1;
	if (nb == 1 || nb == 0 || nb < 0)
	{
		result = 0;
	}
	else
	{
		while (i > 0)
		{
			if (nb % i == 0 && i > 1)
			{
				result = 0;
			}
			i--;
		}
	}
	return (result);
}
